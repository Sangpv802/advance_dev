package advance_dev.Ass5;
import java.util.*;

public class student {
    private String id;
    private String name;
    private double gpa;

    public student(String id, String name, double gpa) {
        this.id = id;
        this.name = name;
        this.gpa = gpa;
    }

    public String getId()
    {
        return id;
    }
    
    public String getName()
    {
        return name;
    }

    
}
